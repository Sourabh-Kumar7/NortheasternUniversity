    private void jRadioButtonShowPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jRadioButtonShowPasswordActionPerformed
        // TODO add your handling code here:
        boolean showPassword = jRadioButtonShowPassword.isSelected();

        if (showPassword) {
            // Show password as plain text
            jPassword.setEchoChar((char) 0); // Set echo char to 0 to display plain text
        } else {
            // Hide password with asterisks (default behavior)
            jPassword.setEchoChar('*');
        }
    }//GEN-LAST:event_jRadioButtonShowPasswordActionPerformed

    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        String username = txtUserName.getText();
        String password = new String(jPassword.getPassword());
        String userType = (String) jComboBoxUserType.getSelectedItem();

        try {
            // Check if username exists
            if (!DataManager.usernamePersonMap.containsKey(username)) {
                throw new Exception("User not found.");
            }

            Person person = DataManager.usernamePersonMap.get(username);
            // Assuming you have a method to verify the hashed password
            if (!HashingUtil.verifyPassword(password, person.getPassword())) {
                throw new Exception("Invalid password.");
            }
            
            if (!person.getUserType().equals(userType)) {
                throw new Exception("Invalid user type.");
            }

            // Check user type and open corresponding portal
            switch (userType) {
                case "Customer" -> {
                    Customer customer = DataManager.customerDirectory.get(person.getPersonId());
                    CustomerPortalJFrame customerPage = new CustomerPortalJFrame(customer);
                    customerPage.setVisible(true);
                }
                case "Sales Person" -> {
                    SalesPerson salesPerson = DataManager.salesPersonDirectory.get(person.getPersonId());
                    SalesJFrame salesPage = new SalesJFrame(salesPerson);
                    salesPage.setVisible(true);
                }
                case "Supplier Owner" -> {
                    Supplier supplier = DataManager.supplierDirectory.get(person.getPersonId());
                    SupplierPortalJFrame supplierPage = new SupplierPortalJFrame(supplier);
                    supplierPage.setVisible(true);
                }
                case "Marketing Manager" -> {
                    MarketManager marketManager = DataManager.marketManagerDirectory.get(person.getPersonId());
                    MarketManagerPortalJFrame marketManagerPage = new MarketManagerPortalJFrame(marketManager);
                    marketManagerPage.setVisible(true);
                }
                case "Admin" -> {
                    BusinessOwnerDashboardJFrame adminPage = new BusinessOwnerDashboardJFrame(person);
                    adminPage.setVisible(true);
                }
                default -> throw new Exception("Invalid user type.");
            }
            
            btnClearActionPerformed(null);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Login Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

