    private void btnCreateChannelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCreateChannelActionPerformed
        String channelName = txtMarketName.getText().trim();
        String channelDescription = txtMarketDecription.getText().trim();

        if (channelName.isEmpty() || channelDescription.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name and description cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Create a new Channel object
        Channel newChannel = new Channel(channelName, channelDescription);
        DataManager.channelDirectory.put(newChannel.getChannelId(), newChannel);

        // Adding the newly created channel ID to the MarketManager's channel list
        if (marketManager != null) {
            marketManager.addChannelId(newChannel.getChannelId());
        }

        JOptionPane.showMessageDialog(this, "Channel created successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
        clearFields();
    }//GEN-LAST:event_btnCreateChannelActionPerformed

