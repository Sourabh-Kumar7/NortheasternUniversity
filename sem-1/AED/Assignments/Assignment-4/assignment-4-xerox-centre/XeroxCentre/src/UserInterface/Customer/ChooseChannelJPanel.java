    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // Get the selected row from the table
        int selectedRow = jTable1.getSelectedRow();
        
        if (selectedRow >= 0) {
            // Get the channel ID from the selected row
            int channelId = (int) jTable1.getValueAt(selectedRow, 0);
            
            // Update the customer's channel ID
            this.customer.setChannelId(channelId);
            
            // Show a message or perform any additional actions as needed
            JOptionPane.showMessageDialog(ChooseChannelJPanel.this, "Channel selected and saved.");
        } else {
            // No row selected, show an error message
            JOptionPane.showMessageDialog(ChooseChannelJPanel.this, "Please select a channel.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    
    private void loadChannelData() {
        // Replace this with code to fetch channel data from your data source or service
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        // Clear existing rows in the table
        model.setRowCount(0);

        // Populate the table with channel data
        for (Channel channel : DataManager.channelDirectory.values()) {
            model.addRow(new Object[] {
                channel.getChannelId(),
                channel.getName()
            });
        }
    }

