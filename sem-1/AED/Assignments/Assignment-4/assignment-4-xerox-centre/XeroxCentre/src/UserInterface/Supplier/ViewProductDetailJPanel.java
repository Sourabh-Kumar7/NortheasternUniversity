    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        try {
            String name = txtProductName.getText().trim();
            double floorPrice = Double.parseDouble(txtLowestPrice.getText().trim());
            double ceilingPrice = Double.parseDouble(txtHighestPrice.getText().trim());
            double targetPrice = Double.parseDouble(txtTargetPrice.getText().trim());
            int quantity = Integer.parseInt(txtQuantity.getText().trim());
            String description = txtAreaProductDescription.getText().trim();

            // Perform additional validations if needed
            if(name.isEmpty() || description.isEmpty()) {
                throw new IllegalArgumentException("Name and description cannot be empty.");
            }
            if(floorPrice <= 0 || ceilingPrice <= 0 || targetPrice <= 0 || quantity < 0) {
                throw new IllegalArgumentException("Price and quantity values must be positive.");
            }

            // Update the product details
            product.setName(name);
            product.setFloorPrice(floorPrice);
            product.setCeilingPrice(ceilingPrice);
            product.setTargetPrice(targetPrice);
            product.setQuantity(quantity);
            product.setDescription(description);

            JOptionPane.showMessageDialog(null, "Product details updated successfully");
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Please enter valid numeric values", "Error", JOptionPane.ERROR_MESSAGE);
        } catch (IllegalArgumentException e) {
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int productId = product.getProductId();
        int supplierId = product.getSupplierId(); // Assuming product has a supplierId field

        if (checkIfProductSoldOrOrdered(productId)) {
            JOptionPane.showMessageDialog(null, "This product has been sold or ordered. You cannot delete it, but you can set its quantity to 0.");
        } else {
            // Remove the product from the product catalog
            DataManager.productCatalog.remove(productId);

            // Remove the product from the supplier's list
            Supplier supplier = DataManager.supplierDirectory.get(supplierId);
            if (supplier != null) {
                supplier.removeProductId(productId);
            }

            JOptionPane.showMessageDialog(null, "Product deleted successfully");
            btnBackActionPerformed(null); // Navigate back after deletion
        }
    }//GEN-LAST:event_btnDeleteActionPerformed
    
    private boolean checkIfProductSoldOrOrdered(int productId) {
        ProductSalesHistory salesHistory = DataManager.productSalesHistoryCatalog.get(productId);
        return salesHistory != null && !salesHistory.getSoldPrices().isEmpty();
    }
    
    private void loadProductDetails() {
        txtProductID.setText(String.valueOf(product.getProductId()));
        txtProductName.setText(product.getName());
        txtLowestPrice.setText(String.valueOf(product.getFloorPrice()));
        txtHighestPrice.setText(String.valueOf(product.getCeilingPrice()));
        txtTargetPrice.setText(String.valueOf(product.getTargetPrice()));
        txtQuantity.setText(String.valueOf(product.getQuantity()));
        txtAreaProductDescription.setText(product.getDescription());
    }

