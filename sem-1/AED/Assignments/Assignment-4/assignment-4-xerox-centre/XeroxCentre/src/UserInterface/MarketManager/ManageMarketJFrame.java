    private void btnViewMarketActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewMarketActionPerformed
        // TODO add your handling code here:
        Point parentFramePosition = this.getLocation();
        this.setVisible(false);
        
        int selectedRow = tblMarket.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(this, "Please select a market to view", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int marketId = (int) tblMarket.getValueAt(selectedRow, 0);
        Market market = DataManager.marketCatalog.get(marketId);
        if (market != null) {
            ViewMarketJFrame viewMarketFrame = new ViewMarketJFrame(this, market);
            viewMarketFrame.setLocation(parentFramePosition);
            viewMarketFrame.setVisible(true);
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnViewMarketActionPerformed

