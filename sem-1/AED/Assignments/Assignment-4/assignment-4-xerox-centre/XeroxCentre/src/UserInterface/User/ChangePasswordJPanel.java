    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        String currentPassword = new String(txtCurrPassword.getPassword()); // Get current password
        String newPassword = new String(txtNewPassword.getPassword()); // Get new password
        String confirmNewPassword = new String(txtConfirmNewPasswd.getPassword()); // Get confirm new password

        // Check if any password field is empty
        if (currentPassword.isEmpty() || newPassword.isEmpty() || confirmNewPassword.isEmpty()) {
            JOptionPane.showMessageDialog(this, "All password fields must be filled.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Hash the current password
        String currentPasswordHash = HashingUtil.hashString(currentPassword);

        // Assuming person.getPassword() returns the hashed password
        String userHashedPassword = person.getPassword();

        // Verify the current password
        if (!currentPasswordHash.equals(userHashedPassword)) {
            JOptionPane.showMessageDialog(this, "Incorrect current password.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Validate the new password
        try {
            validatePassword(newPassword);
        } catch (CustomExceptions.InvalidPasswordException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Ensure new password is not the same as the current password
        if (currentPasswordHash.equals(HashingUtil.hashString(newPassword))) {
            JOptionPane.showMessageDialog(this, "New password cannot be the same as the current password.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Check if new password and confirm new password match
        if (!newPassword.equals(confirmNewPassword)) {
            JOptionPane.showMessageDialog(this, "New password and confirm password do not match.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Update the user's password
        String newHashedPassword = HashingUtil.hashString(newPassword);
        person.setPassword(newHashedPassword); // Assuming a method to set the new hashed password exists

        JOptionPane.showMessageDialog(this, "Password changed successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);

        // Clear the password fields
        txtCurrPassword.setText("");
        txtNewPassword.setText("");
        txtConfirmNewPasswd.setText("");
    }//GEN-LAST:event_btnSaveActionPerformed

