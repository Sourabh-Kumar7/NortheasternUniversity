    private void btnAddProductToBundleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddProductToBundleActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblNotAddedProductInBundle.getSelectedRow();
        if (selectedRow >= 0) {
            DefaultTableModel modelProducts = (DefaultTableModel) tblProductsFromBundle.getModel();
            DefaultTableModel modelNotAddedProducts = (DefaultTableModel) tblNotAddedProductInBundle.getModel();

            Object[] rowData = new Object[]{
                modelNotAddedProducts.getValueAt(selectedRow, 0), // Product ID
                modelNotAddedProducts.getValueAt(selectedRow, 1), // Product Name
                modelNotAddedProducts.getValueAt(selectedRow, 2)  // Target Price
            };

            modelProducts.addRow(rowData); // Add to Products From Bundle table
            modelNotAddedProducts.removeRow(selectedRow); // Remove from Not Added Products table
        } else {
            JOptionPane.showMessageDialog(this, "Please select a product to add to the bundle", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAddProductToBundleActionPerformed

