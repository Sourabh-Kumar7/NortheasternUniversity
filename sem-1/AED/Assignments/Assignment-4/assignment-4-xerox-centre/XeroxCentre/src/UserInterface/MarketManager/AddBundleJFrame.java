    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String bundleName = txtBundleName.getText().trim();
        String bundleDescription = txtBundleDescription.getText().trim();

        if (bundleName.isEmpty() || bundleDescription.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Name and description cannot be empty", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Create a new bundle
        Bundle bundle = new Bundle(bundleName, bundleDescription, market.getId());

        // Add products to the bundle
        for (int i = 0; i < tblProductsFromBundle.getRowCount(); i++) {
            int productId = (Integer) tblProductsFromBundle.getValueAt(i, 0);
            bundle.addProductId(productId);
        }

        // Save bundle to DataManager
        DataManager.bundleCatalog.put(bundle.getId(), bundle);

        // Add bundle to the market
        market.addBundle(bundle.getId());
        DataManager.marketCatalog.put(market.getId(), market);

        JOptionPane.showMessageDialog(this, "Bundle added successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
    }//GEN-LAST:event_btnSaveActionPerformed

