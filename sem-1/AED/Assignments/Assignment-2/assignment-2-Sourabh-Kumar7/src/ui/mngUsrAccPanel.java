    private void btnSearchByIDActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchByIDActionPerformed
        // TODO add your handling code here:
        try {
            int personId = Integer.parseInt(txtID.getText());

            // Check if the personId exists in personMap
            if (!userMap.containsKey(personId)) {
                JOptionPane.showMessageDialog(this, "User with NEU ID " + personId + " does not exist.", "Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method to prevent further processing
            }

            // Get the corresponding person object from personMap
            User u = userMap.get(personId);

            // Open the personView frame and pass the person object
            userViewFrame viewFrame = new userViewFrame(u, mngUser);
            viewFrame.setVisible(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid Person NEU ID. Please enter a valid numeric ID.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSearchByIDActionPerformed


