    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        if (u.equals(mngUsr)) {
            JOptionPane.showMessageDialog(this, "You cannot delete your own account.");
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this user?");
        if (confirm == JOptionPane.YES_OPTION) {
            // Check if the user exists in the DataManager
            if (DataManager.userMap.containsKey(u.getId())) {
                // Remove the user from the userMap and usernameMap
                DataManager.userMap.remove(u.getId());
                DataManager.userByUsernameMap.remove(u.getUsername());

                JOptionPane.showMessageDialog(this, "User has been deleted successfully.");
                System.out.println("User " + u.getUsername() + " has been deleted by " + mngUsr.getUsername());
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete the user.");
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (u.equals(mngUsr)) {
            JOptionPane.showMessageDialog(this, "You cannot update your own account.");
            return;
        }

        String updatedAccountStatus = (String) jComboBoxAccountStatus.getSelectedItem();
        String updatedUserType = (String) jComboBoxUserType.getSelectedItem();
        
        boolean isEnabled = "Enabled".equals(updatedAccountStatus); // true if updatedAccountStatus is "Enabled", false otherwise
        boolean isAdmin = "Admin".equals(updatedUserType);

        // Check if account status or user type is changed
        if (u.getIsEnabled() != isEnabled || u.getIsAdmin() != isAdmin) {
            // Update account status and user type in the user object
            u.setIsEnabled(isEnabled);
            u.setIsAdmin(isAdmin);

            JOptionPane.showMessageDialog(this, "User has been updated successfully.");
            System.out.println("User " + u.getUsername() + " has been updated by " + mngUsr.getUsername());
        } else {
            JOptionPane.showMessageDialog(this, "No changes detected.");
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

    private void btnResetPasswordActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetPasswordActionPerformed
        // TODO add your handling code here:
        String newPassword = PasswordGenerator.generatePassword(12);

        try {
            // Validate the generated password
            validationUtil.validatePassword(newPassword);

            // Hash the password
            String hashedPassword = hashString(newPassword);

            // Update the user's password with the hashed password
            u.setHashedPassword(hashedPassword);

            // Display a message with the new password
            JOptionPane.showMessageDialog(this, "Password reset successful.\nNew Password: " + newPassword);

            // You can also save the updated user object to your data management system here if needed.

        } catch (InvalidPasswordException e) {
            JOptionPane.showMessageDialog(this, "Password reset failed. The generated password is invalid.");
        }
        
    }//GEN-LAST:event_btnResetPasswordActionPerformed

    

