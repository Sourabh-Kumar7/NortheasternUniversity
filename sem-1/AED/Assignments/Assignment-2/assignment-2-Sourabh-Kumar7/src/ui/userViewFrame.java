    private void initFrame() {
        Person person = DataManager.personMap.get(u.getPid());

        txtID.setText(String.valueOf(person.getId()));
        txtName.setText(person.getFirstName());
        txtEmail.setText(person.getEmail());
        txtPhone.setText(person.getPhoneNum());
        txtUserName.setText(u.getUsername());

        // Set account status combo box
        jComboBoxAccountStatus.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Select Account Status", "Enabled", "Disabled"}));
        if (u.getIsEnabled()) {
            jComboBoxAccountStatus.setSelectedItem("Enabled");
        } else {
            jComboBoxAccountStatus.setSelectedItem("Disabled");
        }

        // Set user type combo box
        jComboBoxUserType.setModel(new javax.swing.DefaultComboBoxModel<>(new String[]{"Select User Type", "Normal", "Admin"}));
        if (u.getIsAdmin()) {
            jComboBoxUserType.setSelectedItem("Admin");
        } else {
            jComboBoxUserType.setSelectedItem("Normal");
        }
    }
