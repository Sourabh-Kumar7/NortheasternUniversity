    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // Get the current username and new username from the text fields
        String currentUsername = txtPaneCurrUsrName.getText();
        String newUsername = txtNewUsrName.getText();

        // Check if the current username and new username are the same
        if (currentUsername.equals(newUsername)) {
            JOptionPane.showMessageDialog(this, "New username must be different from the current username.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method
        }

        // Check if the new username already exists in the user map
        if (userByUsernameMap.containsKey(newUsername)) {
            JOptionPane.showMessageDialog(this, "Username already exists. Please choose a different username.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Exit the method
        }
        
        try{
            validateUsername(newUsername);
        }catch (InvalidUsernameException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
                return; // Exit the method
        }
        
        // If everything is fine, update the user object with the new username
        User us = userByUsernameMap.get(currentUsername); // Assuming userMap is your user mapping structure

        if (us != null) {
            us.setUsername(newUsername); // Exit the method

            // Update the user map with the new username
            userByUsernameMap.remove(currentUsername);
            userByUsernameMap.put(newUsername, u);

            // Optionally, display a success message or perform any other actions
            JOptionPane.showMessageDialog(this, "Username updated successfully!");
            txtNewUsrName.setText("");
            txtPaneCurrUsrName.setText(newUsername);
        } else {
            JOptionPane.showMessageDialog(this, "User not found.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnSaveActionPerformed


