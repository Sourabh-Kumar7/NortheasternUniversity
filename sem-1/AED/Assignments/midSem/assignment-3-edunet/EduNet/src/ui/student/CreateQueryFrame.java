    private void btnFillDataActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFillDataActionPerformed
        String courseIDText = txtCourseID.getText();
    
        if (courseIDText.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter a Course ID.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            int courseId = Integer.parseInt(courseIDText);

            // Check if the course exists
            if (!courseMap.containsKey(courseId)) {
                JOptionPane.showMessageDialog(this, "Course not found. Please enter a valid Course ID.", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            Course course = courseMap.get(courseId);
            User professor = userMap.get(course.getInstructorID());

            txtCourseName.setText(course.getCourseName());
            txtProfessorID.setText(String.valueOf(professor.getId()));
            txtProfessorName.setText(professor.getName());
            jTextAreaQueryDesc.setEditable(true);
            txtQueryTitle.setEditable(true);
            btnSubmit.setEnabled(true);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid Course ID. Please enter a valid number.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        
    }//GEN-LAST:event_btnFillDataActionPerformed

    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        String queryTitle = txtQueryTitle.getText();
        String queryDesc = jTextAreaQueryDesc.getText();

        if (queryTitle.isEmpty() || queryDesc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter both a query title and description.", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Get the student, professor, and course IDs
            int studentId = u.getId();
            int professorId = Integer.parseInt(txtProfessorID.getText());
            int courseId = Integer.parseInt(txtCourseID.getText());

            // Create a new StudentQuery
            StudentQuery studentQuery = new StudentQuery(studentId, professorId, courseId, queryTitle, queryDesc);

            // Add the StudentQuery to the appropriate maps
            if (!DataManager.studentQueryMap.containsKey(studentId)) {
                DataManager.studentQueryMap.put(studentId, new ArrayList<>());
            }
            DataManager.studentQueryMap.get(studentId).add(studentQuery);

            if (!DataManager.professorQueryMap.containsKey(professorId)) {
                DataManager.professorQueryMap.put(professorId, new ArrayList<>());
            }
            DataManager.professorQueryMap.get(professorId).add(studentQuery);

            DataManager.queryMap.put(studentQuery.getQueryId(), studentQuery);

            JOptionPane.showMessageDialog(this, "Query submitted successfully.", "Success", JOptionPane.INFORMATION_MESSAGE);
            StudentPortalJFrame studentPortal = new StudentPortalJFrame(this.u);
            studentPortal.setVisible(true);
            studentPortal.loadProfessorEngagementPanel();
            dispose(); // Close the second frame
            btnClearActionPerformed(null);
        }
    }//GEN-LAST:event_btnSubmitActionPerformed

