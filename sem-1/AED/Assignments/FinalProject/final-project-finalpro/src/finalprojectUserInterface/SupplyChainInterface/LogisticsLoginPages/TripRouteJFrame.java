    private void btnShowMapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnShowMapActionPerformed
        // TODO add your handling code here:
        String origin = txtFrom.getText().trim();
        String destination = txtTo.getText().trim();

        // Check if both "From" and "To" locations are provided
        if (origin.isEmpty() || destination.isEmpty()) {
            // Display an error message if either field is empty
            JOptionPane.showMessageDialog(this, "Please enter both 'From' and 'To' locations.",
                    "Error", JOptionPane.ERROR_MESSAGE);
        } else {
            // Create the Google Maps URL
            String googleMapsUrl = "https://www.google.com/maps/dir/?api=1&origin=" + origin + "&destination=" + destination;

            // Open the URL in the default web browser
            try {
                Desktop.getDesktop().browse(new URI(googleMapsUrl));
            } catch (IOException | URISyntaxException e) {
                e.printStackTrace();
                // Handle any exceptions that might occur during the browser opening process
                JOptionPane.showMessageDialog(this, "Error opening Google Maps. Please check your internet connection.",
                        "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnShowMapActionPerformed

