    private void signup_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_signup_btnActionPerformed
        // TODO add your handling code here:
        try {
            if(EmpytyFieldValidation())
            {
                if(RegexValidation())
                {
                    String address = add_txt.getText() + "," + city_txt.getText() + "," + statename_txt.getText() + "," + zipcode_txt.getText();
                    BankDonor donbank = new BankDonor(address,uname_txt.getText(),pwd_txt.getText(),name_txt.getText(), num_txt.getText());
                    operatingSystem.addBankDonar(donbank);
                    System.out.println("Adding Donation Centre Admin User: " + uname_txt.getText());
                    dB4OUtility.storeSystem(operatingSystem);
                    MainJFrameForm suc = new MainJFrameForm();
                    ((JFrame) SwingUtilities.getWindowAncestor(this)).dispose();
                    suc.setVisible(true);
                }
                else
                {
                    JOptionPane.showMessageDialog(this,"Some Error in entered data.Please check over the red fields to know more.");
                    validationCheck=true;
                }
            }
            else{
                JOptionPane.showMessageDialog(this,"Data Cant be empty. Please check over the red fields to know more.");
                ValidationStatus=true;
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Donor Admin not registered, Try again");
            ValidationStatus=true;
        }
        
    }//GEN-LAST:event_signup_btnActionPerformed

