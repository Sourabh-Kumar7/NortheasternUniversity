    private void login_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_login_btnActionPerformed
        // TODO add your handling code here:
        try{
            if (role_cmb.getSelectedItem().toString() == "NGO Admin") {
                NGOClass ho = (NGOClass) operatingSystem.loginAuthentication(uname_txt.getText(), pwd_txt.getText());
                if(ho == null){
                    JOptionPane.showMessageDialog(this, "Incorrect credential");
                }else{
                    NGOAdminLogin ap = new NGOAdminLogin(MainLoginPage, dB4OUtility, operatingSystem, ho);
                    MainLoginPage.setContentPane(ap);
                    MainLoginPage.invalidate();
                    MainLoginPage.validate();
                    return;
                }

            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"NGO does not exist. Try again");
        }
        
        
        try {
            if (role_cmb.getSelectedItem().toString() == "Receiver") {
            System.out.println("selecteditem");
            Recepient pa = (Recepient) operatingSystem.loginAuthentication(uname_txt.getText(), pwd_txt.getText());
            if (pa == null) {
                JOptionPane.showMessageDialog(this, "Incorrect credential");
            } else {
                ReceiverLogin pl = new ReceiverLogin(MainLoginPage, dB4OUtility, operatingSystem, pa);
                MainLoginPage.setContentPane(pl);
                MainLoginPage.invalidate();
                MainLoginPage.validate();
                return;
            }
        }
        } catch(Exception e) {
            JOptionPane.showMessageDialog(this,"Receiver does not exist. Try again");
            //System.out.println(e.getMessage());
        }

        try {
            if (role_cmb.getSelectedItem().toString() == "NGO Manager") {
            System.out.println("selecteditem");
            Manager dr = (Manager) operatingSystem.loginAuthentication(uname_txt.getText(), pwd_txt.getText());
            if (dr == null) {
                JOptionPane.showMessageDialog(this, "Incorrect credential");
            } else {
                ManagerLogin dl = new ManagerLogin(MainLoginPage, dB4OUtility, operatingSystem, dr);
                MainLoginPage.setContentPane(dl);
                MainLoginPage.invalidate();
                MainLoginPage.validate();
                return;
            }
        }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Manager does not exist. Try again");
        }
        
        
        try {
            if (role_cmb.getSelectedItem().toString() == "Caretaker") {
            JOptionPane.showMessageDialog(this, "Caretakers not allowed to login, contact Administrator");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Caretakers not allowed to login, contact Administrator");
        }
        
    }//GEN-LAST:event_login_btnActionPerformed

