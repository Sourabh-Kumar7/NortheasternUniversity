    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String personId = IDTextField.getText();
        String name = nameTextField.getText();
        String specialty = specialityTextField.getText();
        String licenseNumber = licenseTextField.getText();
        String education = educationTextField.getText();
        String hospitalAffiliation = affiliationTextField.getText();
        String contactNumber = numberTextField.getText();
        String email = emailTextField.getText();

        // Check if any required field is empty
        if (personId.isEmpty() || name.isEmpty() || specialty.isEmpty() || licenseNumber.isEmpty() || education.isEmpty() || hospitalAffiliation.isEmpty() || email.isEmpty() || contactNumber.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields.", "Missing Information", JOptionPane.ERROR_MESSAGE);
            return; // Don't save if any field is empty
        }
        
        try {
            int pId = Integer.parseInt(personId);
            int licNum = Integer.parseInt(licenseNumber);
            int cNum = Integer.parseInt(contactNumber);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        
        // Set the values to the MedicalDoctor object
        medicalDoctor.setPersonId(personId);
        medicalDoctor.setName(name);
        medicalDoctor.setSpecialty(specialty);
        medicalDoctor.setLicenseNumber(licenseNumber);
        medicalDoctor.setEducation(education);
        medicalDoctor.setHospitalAffiliation(hospitalAffiliation);
        medicalDoctor.setContactNumber(contactNumber);
        medicalDoctor.setEmail(email);
        JOptionPane.showMessageDialog(null, "Data saved successfully.");
    }//GEN-LAST:event_btnSaveActionPerformed


