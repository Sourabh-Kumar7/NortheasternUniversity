    private void btnSave6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave6ActionPerformed
        // TODO add your handling code here:
        String vehicleRegistrationNumber = regNumTextField.getText();
        String vehicleMake = manufacturerTextField.getText();
        String vehicleModel = modelTextField.getText();
        String manuYear = manuYearTextField.getText();
        String vehicleColor = vehColorTextField.getText();

        // Check if any required field is empty
        if (vehicleRegistrationNumber.isEmpty() || vehicleMake.isEmpty() || vehicleMake.isEmpty() || manuYear.isEmpty() || vehicleColor.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields.", "Missing Information", JOptionPane.ERROR_MESSAGE);
            return; // Don't save if any field is empty
        }
        
        try {
            int vehNum = Integer.parseInt(vehicleRegistrationNumber);
            int manYear = Integer.parseInt(manuYear);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            int manufacturingYear = Integer.parseInt(manuYearTextField.getText());
            vehReg.setVehicleRegistrationNumber(vehicleRegistrationNumber);
            vehReg.setVehicleMake(vehicleMake);
            vehReg.setVehicleModel(vehicleModel);
            vehReg.setManufacturingYear(manufacturingYear);
            vehReg.setVehicleColor(vehicleColor);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid number.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        JOptionPane.showMessageDialog(null, "Data saved successfully.");

    }//GEN-LAST:event_btnSave6ActionPerformed

