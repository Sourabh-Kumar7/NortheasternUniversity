    private void btnSave4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave4ActionPerformed
        // TODO add your handling code here:
        // Retrieve values from input fields
        String corporateIdNumber = CorporateIDTextField.getText();
        String address = addressTextField.getText();
        String prem = premiumTextField.getText();
        String deduc = deductibleTextField.getText();
        String benefitsDescription = benefitTextField.getText();

        // Check if any required field is empty
        if (corporateIdNumber.isEmpty() || address.isEmpty() || prem.isEmpty() || deduc.isEmpty() || benefitsDescription.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields.", "Missing Information", JOptionPane.ERROR_MESSAGE);
            return; // Don't save if any field is empty
        }
        
        try {
            int cNum = Integer.parseInt(corporateIdNumber);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }

        try {
            
            double premium = Double.parseDouble(premiumTextField.getText());
            double deductible = Double.parseDouble(deductibleTextField.getText());
            // Set the values to the InsuranceCoverage object
            insuranceCoverage.setCorporateIdNumber(corporateIdNumber);
            insuranceCoverage.setCompanyAddress(address);
            insuranceCoverage.setPremium(premium);
            insuranceCoverage.setDeductible(deductible);
            insuranceCoverage.setBenefitsDescription(benefitsDescription);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid input.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        JOptionPane.showMessageDialog(null, "Data saved successfully.");
        
    }//GEN-LAST:event_btnSave4ActionPerformed

