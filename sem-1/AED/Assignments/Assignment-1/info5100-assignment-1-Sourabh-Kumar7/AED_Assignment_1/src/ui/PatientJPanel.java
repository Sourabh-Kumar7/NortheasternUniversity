    private void btnSave4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave4ActionPerformed
        // TODO add your handling code here:
        // Retrieve values from input fields
        String pId = patientIDTextField.getText();
        String doctor = doctorTextField.getText();
        Date lastVisitDate = lastVisitDateChooser.getDate();
        Date nextAppointmentDate = nextVisitDateChooser2.getDate();
        boolean allergies = jComboBox1.getSelectedItem().toString().equalsIgnoreCase("Yes");
        boolean onMedication = jComboBox2.getSelectedItem().toString().equalsIgnoreCase("Yes");

        // Check if any required field is empty
        if (doctor.isEmpty() || pId.isEmpty() || lastVisitDate == null || nextAppointmentDate == null) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields.", "Missing Information", JOptionPane.ERROR_MESSAGE);
            return; // Don't save if any field is empty
        }
        
        try {
            int patientId = Integer.parseInt(patientIDTextField.getText());
            patient.setPatientId(patientId);
            patient.setPrimaryDoctor(doctor);
            patient.setLastVisitDate(lastVisitDate);
            patient.setNextAppointmentDate(nextAppointmentDate);
            patient.setAllergies(allergies);
            patient.setOnMedication(onMedication);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid number.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        JOptionPane.showMessageDialog(null, "Data saved successfully.");
    }//GEN-LAST:event_btnSave4ActionPerformed

