    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        String streetAddress = streetTextField.getText();
        String nameValue = nameTextField.getText();
        String apartmentNumber = apratmentTextField.getText();
        String cityName = cityTextField.getText();
        String stateName = stateTextField.getText();
        String countryName = countryTextField.getText();
        String postalCodeValue = postalCodeTextField.getText();

        // Check if any required field is empty
        if (streetAddress.isEmpty() || nameValue.isEmpty() || cityName.isEmpty() || apartmentNumber.isEmpty() || stateName.isEmpty() || countryName.isEmpty() || postalCodeValue.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields.", "Missing Information", JOptionPane.ERROR_MESSAGE);
            return; // Don't save if any field is empty
        }
        
        try {
            int aptNum = Integer.parseInt(apartmentNumber);
            int zipCode = Integer.parseInt(postalCodeValue);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
                
        // Set the values to the Address object
        address.setStreet(streetAddress);
        address.setName(nameValue);
        address.setApartment(apartmentNumber);
        address.setCity(cityName);
        address.setState(stateName);
        address.setCountry(countryName);
        address.setPostalCode(postalCodeValue);
        JOptionPane.showMessageDialog(null, "Data saved successfully.");
    }//GEN-LAST:event_btnSaveActionPerformed


