    private void btnSave4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSave4ActionPerformed
        // TODO add your handling code here:
        String licenseNumber = licenseNumberTextField.getText();
        String issuingAuthority = issueAuthorityTextField.getText();
        Date issueDate = issueDateChooser.getDate();
        Date expirationDate = expirationDateChooser.getDate();

        // Check if any required field is empty
        if (licenseNumber.isEmpty() || issuingAuthority.isEmpty() || issueDate == null || expirationDate == null) {
            JOptionPane.showMessageDialog(this, "Please fill in all required fields.", "Missing Information", JOptionPane.ERROR_MESSAGE);
            return; // Don't save if any field is empty
        }
        
        try {
            int licNum = Integer.parseInt(licenseNumber);
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(null, "Invalid input. Please enter a valid integer.", "Input Error", JOptionPane.ERROR_MESSAGE);
            return;
        }


        // Set the values to the DriverLicense object
        driverLicense.setLicenseNumber(licenseNumber);
        driverLicense.setIssuingAuthority(issuingAuthority);
        driverLicense.setIssueDate(issueDate);
        driverLicense.setExpirationDate(expirationDate);
        JOptionPane.showMessageDialog(null, "Data saved successfully.");
    }//GEN-LAST:event_btnSave4ActionPerformed

