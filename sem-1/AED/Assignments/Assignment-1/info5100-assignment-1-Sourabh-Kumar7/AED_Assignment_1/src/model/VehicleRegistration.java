/**
 * The {@code VehicleRegistration} class represents the registration details of a vehicle,
 * including attributes such as vehicle registration number, make, model, manufacturing year,
 * color, and the associated person's ID.
 */
public class VehicleRegistration {
    private String vehicleRegistrationNumber; // Vehicle registration number (license plate)
    private String vehicleMake;               // Make or manufacturer of the vehicle
    private String vehicleModel;              // Model of the vehicle
    private int manufacturingYear;            // Year the vehicle was manufactured
    private String vehicleColor;              // Color of the vehicle
    private String personId;                  // Person's ID associated with the registration

    public String getVehicleRegistrationNumber() {
        return vehicleRegistrationNumber;
    }

    public void setVehicleRegistrationNumber(String vehicleRegistrationNumber) {
        this.vehicleRegistrationNumber = vehicleRegistrationNumber;
    }

    public String getVehicleMake() {
        return vehicleMake;
    }

    public void setVehicleMake(String vehicleMake) {
        this.vehicleMake = vehicleMake;
    }

    public String getVehicleModel() {
        return vehicleModel;
    }

    public void setVehicleModel(String vehicleModel) {
        this.vehicleModel = vehicleModel;
    }

    public int getManufacturingYear() {
        return manufacturingYear;
    }

    public void setManufacturingYear(int manufacturingYear) {
        this.manufacturingYear = manufacturingYear;
    }

    public String getVehicleColor() {
        return vehicleColor;
    }

    public void setVehicleColor(String vehicleColor) {
        this.vehicleColor = vehicleColor;
    }

    public String getPersonId() {
        return personId;
    }

    public void setPersonId(String personId) {
        this.personId = personId;
    }

    
}
